package comparing;

import java.time.LocalDate;
import java.util.Date;

public class Student {
	private int id;
	private String name;
	private LocalDate dateOfBirth;
	private int score;

	public Student(int id, String name, LocalDate dateOfBirth, int score) {
		this.id = id;
		this.name = name;
		this.dateOfBirth = dateOfBirth;
		this.score = score;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public LocalDate getDateOfBirth() {
		return dateOfBirth;
	}

	public void setDateOfBirth(LocalDate dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
	 @Override
	public String toString() {
		return "ID : " +  id + "\tName : "+ name+"\tDateOfBirth : "+ dateOfBirth + "\tScore : " + score;
	}
	
	
}
